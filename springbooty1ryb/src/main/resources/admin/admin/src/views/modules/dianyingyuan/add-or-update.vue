        let reg=new RegExp('../../../upload','g')//g代表全部
        this.ruleForm.yingyuanjieshao = this.ruleForm.yingyuanjieshao.replace(reg,'../../../springbooty1ryb/upload');
        } else {
          this.$message.error(data.msg);
        }
      });
